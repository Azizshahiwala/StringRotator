    private void appCanvas_loadPNGActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_appCanvas_loadPNGActionPerformed
        // TODO add your handling code here:
        LoadPNGMenu();

         int Result = chooser.showOpenDialog(appCanvas_loadPNG);
         
    if(Result == JFileChooser.APPROVE_OPTION){
        appCanvas_load.setVisible(false);
        PNGSelcted = chooser.getSelectedFile();
        appCanvas_imgpathlabel.setText("File: " + PNGSelcted.getAbsolutePath());
        chooser.setCurrentDirectory(PNGSelcted);
            try {
                PNGMode = true;
canvaswindowui = new CanvasWindowUI(this);
canvaswindowui.setVisible(true); 
canvaswindowui.revalidate();
canvaswindowui.repaint();

// Start heavy Python process in a background thread
new Thread(() -> {
    canvaswindowui.startProcess();
    PNGMode = false;
}).start();  
} catch (Exception e) {
    JOptionPane.showMessageDialog(null, "Failed to load PNG canvas" + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }   
    finally{
        appCanvas_load.setVisible(true);
        PNGMode = false;
    }      
    }
}//GEN-LAST:event_appCanvas_loadPNGActionPerformed

