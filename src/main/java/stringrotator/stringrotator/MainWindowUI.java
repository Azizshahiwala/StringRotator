    private void appCanvas_loadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_appCanvas_loadActionPerformed
        // TODO add your handling code here:
        Process process = null;
        //Trim text temporarily, If its empty then throw error msg
        if(manualTextInput.getText().trim().isEmpty()){
        JOptionPane.showMessageDialog(null,"Text cannot be blank.");
        return;
        }

        if(manualTextInput.getText().length() > 10){
            JOptionPane.showMessageDialog(null,"Long messages will get distorted.","Warning",JOptionPane.WARNING_MESSAGE);
        }

        ProcessBuilder pb = new ProcessBuilder(
    "python3","Runner.py",CanvasWindowUI.SharedText_path, "0");  
        try {
            process = pb.start();
            
        } catch (Exception e) {
            // TODO: handle exception
            JOptionPane.showMessageDialog(null, e,"Error", JOptionPane.ERROR_MESSAGE);
        }
        finally{
            if(process != null) process.destroy();
        }
        try{
            //Setup a temporary config file for canvas_window

        config = new FileWriter(".config.txt");
        config.write("Halt = False\nSpeed = "+CanvasWindowUI.speed+"\nSave = "+Save);
        config.close();
        }catch(IOException e){
            JOptionPane.showMessageDialog(null, e,"Error", JOptionPane.ERROR_MESSAGE);
        };
        manualtextinputbuffer.replace(0, manualtextinputbuffer.length(), manualTextInput.getText());
        appCanvas_imgpathlabel.setText("");
        appCanvas_run.setEnabled(true);

    }//GEN-LAST:event_appCanvas_loadActionPerformed

